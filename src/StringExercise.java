public class StringExercise {

    public static void main(String [] args){

        String string1 = "We don't need no education.\nWe don't need no thought control.\n";
        System.out.println(string1);

        String string2 = "Check \"this\" out! \" inside of \"s";
        System.out.println(string2);

        String string3= "In windows, the main drive is usually C:\\";
        System.out.println(string3);

        String string4 = "I can do backslashes \\, double backslashes \\\\," +
                "and the amazing triple backslash \\\\\\!\n";
        System.out.println(string4);

    }
}
